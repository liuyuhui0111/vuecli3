<template>
  <div class="signup common-h5-signup">
    <div class="form">
      <el-form
        ref="formDialog"
        :label-position="labelPosition"
        label-width="0px"
        :model="onlineForm">
        <el-form-item prop="name">
        <div class="item">
          <span class="label">称呼</span>
          <el-input placeholder="请输入称呼"
          v-model.trim="onlineForm.name"></el-input>
        </div>

        </el-form-item>

        <el-form-item prop="tel">
        <div class="item">
          <span class="label">电话</span>
          <el-input placeholder="请输入电话"
          v-model.trim="onlineForm.tel"></el-input>
        </div>
        </el-form-item>


        <el-form-item prop="comp">
        <div class="item">
          <span class="label">公司</span>
          <el-input placeholder="请输入公司"
          v-model.trim="onlineForm.comp"></el-input>
        </div>
        </el-form-item>


         <el-form-item prop="work">
          <div class="item noborder">
          <span class="label">职位</span>
          <el-input placeholder="请输入职位"
          v-model.trim="onlineForm.work"></el-input>
          </div>
        </el-form-item>

        </el-form>
    </div>

    <div class="mes-box">
      <p>您要咨询的内容</p>
      <el-form label-width="0px">
        <el-form-item prop="message">
          <el-input type="textarea"
          placeholder="请输入您的问题"
           v-model.trim="onlineForm.message"></el-input>
        </el-form-item>
      </el-form>
    </div>

    <span @click="submitForm()" class="btn-sub">提交内容</span>
  </div>
</template>
<script>
import {
  offlineCourseSignUp,
} from '@/api/apis';
import { validByPhone } from '@/assets/utils/validator';

export default {
  name: 'signup',
  data() {
    return {
      name: 'signup',
      labelPosition: 'left',
      onlineForm: { // 在线报名表单
        name: '',
        tel: '',
        comp: '',
        work: '',
        message: '',
      },
      courseId: '',
      isCanSub: true,

    };
  },
  mounted() {
    this.init();
  },
  methods: {
    init() {
      this.courseId = this.$route.query.id;
    },
    submitForm() {
      if (!this.onlineForm.name) {
        this.$message({
          showClose: true,
          message: '请输入称呼',
          type: 'warning',
          duration: 2000,
        });
        return;
      }
      if (!this.onlineForm.tel) {
        this.$message({
          message: '请输入电话',
          type: 'warning',
          duration: 2000,
        });
        return;
      }
      if (!validByPhone(this.onlineForm.tel)) {
        this.$message({
          message: '电话号码格式不对',
          type: 'warning',
          duration: 2000,
        });
        return;
      }
      if (!this.onlineForm.comp) {
        this.$message({
          message: '请输入公司',
          type: 'warning',
          duration: 2000,
        });
        return;
      }
      if (!this.onlineForm.work) {
        this.$message({
          message: '请输入职位',
          type: 'warning',
          duration: 2000,
        });
        return;
      }
      if (!this.onlineForm.message) {
        this.$message({
          message: '请输入您要咨询的问题',
          type: 'warning',
          duration: 2000,
        });
        return;
      }
      if (this.isCanSub) {
        this.isCanSub = false;
      } else {
        return;
      }
      this.offlineCourseSignUpFn();
    },
    offlineCourseSignUpFn() {
      // 线上课在线报名提交表单
      let params = {
        company: this.onlineForm.comp,
        offlineCourseId: this.courseId,
        name: this.onlineForm.name,
        phone: this.onlineForm.tel,
        job: this.onlineForm.work,
        content: this.onlineForm.message,
      };
      offlineCourseSignUp(params).then((res) => {
        this.isCanSub = true;
        if (res.data.code === '0000') {
          // this.detailData = res.data.data
          let oThis = this;
          this.$message({
            message: '报名成功',
            type: 'success',
            onClose() {
              oThis.$router.replace({ path: '/h5/success' });
            },
          });
        }
      }).catch((err) => {
        this.isCanSub = true;
        console.log(err);
        this.$message({
          message: '公开课详情获取失败，请稍后再试',
          type: 'warning',
        });
      });
    },
  },
};
</script>
